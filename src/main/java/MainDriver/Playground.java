    private void updateLabel() {
        java.util.Iterator<String> i = this.testQueue.getIterator();
        String s = "";
        i.forEachRemaining((String data) -> {
            System.out.println(data);
        });
        i = this.testQueue.getIterator();
        while(i.hasNext()) {
            s += i.next() + "\n";
        }
        jLabel1.setText(s);
    }

